// Хелперы для работы с данными
export const getAllPrograms = (): Program[] => {
  return programCategories.flatMap(cat => cat.programs);
};

export const getProgramById = (id: string): Program | undefined => {
  return getAllPrograms().find(program => program.id === id);
};

export const getProgramsByCategory = (categoryId: string): Program[] => {
  const category = programCategories.find(cat => cat.id === categoryId);
  return category ? category.programs : [];
};

